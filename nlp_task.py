@tool(
    name="comprehensive_news_analysis",
    description="Perform comprehensive analysis of news articles on a given topic",
    show_result=True,
    cache_results=True,
    cache_ttl=1800
)
def analyze_news_topic(topic: str, num_articles: int = 3) -> str:
    """
    Comprehensive analysis of a news topic including entity extraction and summarization.

    Args:
        topic (str): Topic to analyze
        num_articles (int): Number of articles to analyze

    Returns:
        str: Formatted analysis report
    """
    print(f"ðŸ” Analyzing topic: '{topic}'")

    # Search for relevant articles
    articles = search_articles(topic, num_articles)

    if not articles:
        return f"âŒ No relevant articles found for topic: '{topic}'"

    # Combine article texts
    combined_text = ' '.join([article['combined_text'] for article in articles])

    # Extract information
    entities_ner = extract_entities_ner(combined_text)
    entities_regex = extract_entities_regex(combined_text)
    keywords = get_top_keywords(combined_text, 8)

    # Generate summaries
    extractive_summary = extractive_summarize(combined_text, 3)
    abstractive_summary = simple_abstractive_summarize(combined_text, 60)

    # Perform evaluation
    evaluation = evaluator.evaluate_entity_extraction(combined_text, entities_ner, entities_regex)
    summary_eval = evaluator.evaluate_summarization(combined_text, extractive_summary, abstractive_summary)

    # Format report
    report_parts = [
        f"ðŸ“° NEWS ANALYSIS REPORT: {topic.upper()}",
        "=" * 50,
        f"ðŸ“Š Articles analyzed: {len(articles)}",
        f"ðŸ“‚ Categories: {', '.join(set(article['category'] for article in articles))}",
        "",
        "ðŸ” KEY FINDINGS:",
        f"â€¢ Total entities found: {evaluation['quality_assessment']['total_entities_ner'] + evaluation['quality_assessment']['total_entities_regex']}",
        f"â€¢ Entity coverage: {evaluation['quality_assessment']['entity_coverage']}",
        f"â€¢ Content diversity: {evaluation['quality_assessment']['diversity_score']} types",
        "",
        "ðŸ‘¥ KEY ENTITIES:",
    ]

    # Add entity information
    if entities_ner:
        for entity_type, entities in list(entities_ner.items())[:3]:
            if entities:
                report_parts.append(f"â€¢ {entity_type}: {', '.join(entities[:3])}")

    if entities_regex:
        for pattern_type, patterns in entities_regex.items():
            if patterns:
                report_parts.append(f"â€¢ {pattern_type}: {', '.join(patterns[:2])}")

    report_parts.extend([
        "",
        "ðŸ”‘ TOP KEYWORDS:",
    ])

    for word, score in keywords[:5]:
        report_parts.append(f"â€¢ {word}: {score:.3f}")

    report_parts.extend([
        "",
        "ðŸ“‹ EXTRACTIVE SUMMARY:",
        f"{extractive_summary}",
        "",
        "ðŸŽ¯ ABSTRACTIVE SUMMARY:",
        f"{abstractive_summary}",
        "",
        "ðŸ“Š SUMMARY QUALITY METRICS:",
        f"â€¢ Extractive compression: {summary_eval['extractive_evaluation']['compression']['compression_percentage']:.1f}%",
        f"â€¢ Abstractive compression: {summary_eval['abstractive_evaluation']['compression']['compression_percentage']:.1f}%",
        f"â€¢ Content preservation: {summary_eval['extractive_evaluation']['content_preservation']['preservation_quality']}",
        f"â€¢ Coherence score: {summary_eval['extractive_evaluation']['coherence']['coherence_score']:.2f}",
        "",
        "ðŸ“‘ TOP ARTICLES ANALYZED:",
    ])

    for i, article in enumerate(articles[:3], 1):
        report_parts.append(f"{i}. {article['headline']} ({article['category']})")

    return '\n'.join(report_parts)

